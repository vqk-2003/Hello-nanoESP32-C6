#![no_std]
#![no_main]
#![deny(
    clippy::mem_forget,
    reason = "mem::forget is generally not safe to do with esp_hal types, especially those \
    holding buffers for the duration of a data transfer."
)]
#![deny(clippy::large_stack_frames)]

use esp_backtrace as _;
use esp_hal::clock::CpuClock;
use esp_hal::delay::Delay;
use esp_hal::gpio::Level;
use esp_hal::rmt::{Channel, PulseCode, Rmt, Tx, TxChannelConfig, TxChannelCreator};
use esp_hal::time::Rate;
use esp_hal::{Blocking, main};

esp_bootloader_esp_idf::esp_app_desc!();

#[allow(
    clippy::large_stack_frames,
    reason = "it's not unusual to allocate larger buffers etc. in main"
)]
#[main]
fn main() -> ! {
    esp_println::logger::init_logger_from_env();

    let config = esp_hal::Config::default().with_cpu_clock(CpuClock::max());
    let peripherals = esp_hal::init(config);

    let rmt = Rmt::new(peripherals.RMT, Rate::from_mhz(5)).unwrap();
    let mut channel = rmt
        .channel0
        .configure_tx(
            peripherals.GPIO8,
            TxChannelConfig::default()
                .with_clk_divider(1)
                .with_idle_output(true),
        )
        .unwrap();

    let delay = Delay::new();
    let mut r = 0x7F;
    let mut g = 0x7F;
    let mut b = 0x7F;
    let mut led = Ws2812b::init(r, g, b);

    loop {
        delay.delay_millis(500);
        led.change_led(r, g, b);
        channel = led.update(channel);
        let tmp = r;
        r = g;
        g = b;
        b = tmp;
    }
}

struct Ws2812b {
    data: [PulseCode; 25],
}

const LOGIC_ONE: PulseCode = PulseCode::new(Level::High, 4, Level::Low, 2);
const LOGIC_ZERO: PulseCode = PulseCode::new(Level::High, 2, Level::Low, 4);

impl Ws2812b {
    fn init(r: u8, g: u8, b: u8) -> Self {
        let mut led = Self {
            data: [PulseCode::end_marker(); 25],
        };
        led.change_led(r, g, b);
        led
    }

    fn change_led(&mut self, r: u8, g: u8, b: u8) {
        Self::convert_pulses(&mut self.data[0..8], g);
        Self::convert_pulses(&mut self.data[8..16], r);
        Self::convert_pulses(&mut self.data[16..24], b);
    }

    fn update<'a>(&mut self, channel: Channel<'a, Blocking, Tx>) -> Channel<'a, Blocking, Tx> {
        // Because of the extra duplicated pulse generated by the RMT module,
        // the LSB of the previous color becomes the MSB of the next
        // color, so the current solution is to skip transmitting the MSB of the data.
        // As a result, the color green lost a digit of its color value
        channel.transmit(&self.data[1..]).unwrap().wait().unwrap()
    }

    fn convert_pulses(arr: &mut [PulseCode], mut value: u8) {
        if arr.len() != 8 {
            panic!("Wrong number of array length");
        }

        const MASK: u8 = 1 << 7;
        for e in arr {
            *e = if (value & MASK) != 0 {
                LOGIC_ONE
            } else {
                LOGIC_ZERO
            };
            value <<= 1;
        }
    }
}
